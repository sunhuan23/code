# coding=utf-8
"""
数据类型
"""
# 整型（int）
a = 1
print(type(a))

# 浮点（float）
b = 2.0
print(type(b))

# 布尔值
f = False
T = True
print(type(f))
print(type(T))


# 字符串（str）
s = "hello"
print(type(s))

# 列表（list）
l = [1, 2, "hha"]
print(type(l))

# 元祖（tuple）
t = (1, 2, "hha")
print(type(t))

# 字典（dict）
d = {"name":"小米","age":2}
print(type(d))

# 元祖（set）
e = {"a", "b", 3}
print(type(e))